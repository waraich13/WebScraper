package com.webscraper.WebScraper;

public class Car {
	
	public Car() {
		
	}
	
	private String Name;
	private String Price;
	private String Kilometers;
	private String Status;
	private String Trim;
	private String Bodytype;
	private String Engine;
	private int Cylinder;
	private String Transmission;
	private String Drivetrain;
	private int Stocknumber;
	private String Exteriorcolor;
	private String Interiorcolor;
	private int Doors;
	private String Fueltype;
	private String Cityfuel;
	private String Hwyfuel;
		
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getPrice() {
		return Price;
	}
	public void setPrice(String price) {
		Price = price;
	}
	public String getKilometers() {
		return Kilometers;
	}
	public void setKilometers(String kilometers) {
		Kilometers = kilometers;
	}
	public String getStatus() {
		return Status;
	}
	public void setStatus(String status) {
		Status = status;
	}
	public String getTrim() {
		return Trim;
	}
	public void setTrim(String trim) {
		Trim = trim;
	}
	public String getBodytype() {
		return Bodytype;
	}
	public void setBodytype(String bodytype) {
		Bodytype = bodytype;
	}
	public String getEngine() {
		return Engine;
	}
	public void setEngine(String engine) {
		Engine = engine;
	}
	public int getCylinder() {
		return Cylinder;
	}
	public void setCylinder(int cylinder) {
		Cylinder = cylinder;
	}
	public String getTransmission() {
		return Transmission;
	}
	public void setTransmission(String transmission) {
		Transmission = transmission;
	}
	public String getDrivetrain() {
		return Drivetrain;
	}
	public void setDrivetrain(String drivetrain) {
		Drivetrain = drivetrain;
	}
	public int getStocknumber() {
		return Stocknumber;
	}
	public void setStocknumber(int stocknumber) {
		Stocknumber = stocknumber;
	}
	public String getExteriorcolor() {
		return Exteriorcolor;
	}
	public void setExteriorcolor(String exteriorcolor) {
		Exteriorcolor = exteriorcolor;
	}
	public String getInteriorcolor() {
		return Interiorcolor;
	}
	public void setInteriorcolor(String interiorcolor) {
		Interiorcolor = interiorcolor;
	}
	public int getDoors() {
		return Doors;
	}
	public void setDoors(int doors) {
		Doors = doors;
	}
	public String getFueltype() {
		return Fueltype;
	}
	public void setFueltype(String fueltype) {
		Fueltype = fueltype;
	}
	public String getCityfuel() {
		return Cityfuel;
	}
	public void setCityfuel(String cityfuel) {
		Cityfuel = cityfuel;
	}
	public String getHwyfuel() {
		return Hwyfuel;
	}
	public void setHwyfuel(String hwyfuel) {
		Hwyfuel = hwyfuel;
	}

	
	
}
